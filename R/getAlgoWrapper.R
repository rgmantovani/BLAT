getAlgoWrapper = function(lrn, outer.cv, measures) {

  function(job, data, instance, ...) {
    ret = resample(learner = makeLearner(lrn), task = data, resampling = outer.cv, 
      measures = measures, models = FALSE, show.info = TRUE)
    return(ret)
  }
}

