resubmitJobs = function(submit.errors = FALSE) {

  devtools::load_all()

  reg = loadRegistry(file.dir = "test-files")
  ids = findNotDone(reg = reg)

  submitJobs(reg = reg, ids = ids, job.delay = TRUE)

  status = waitForJobs(reg = reg, ids = ids)
  cat("Jobs running again ... \n!")

}

# -------------------------------------------------------------------------------------------------
# -------------------------------------------------------------------------------------------------

resubmitJobs()

